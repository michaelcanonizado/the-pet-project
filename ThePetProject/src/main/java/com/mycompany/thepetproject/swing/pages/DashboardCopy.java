    private void handleRowClick(int row) {
        EditPet editPet = new EditPet();
        UUID id = UUID.fromString((String) model.getValueAt(row, 0));
        String name = (String) model.getValueAt(row, 1);
        int age = Integer.parseInt(model.getValueAt(row, 2).toString());
        PetType type = PetType.valueOf((String) model.getValueAt(row, 3));
        PetSex sex = PetSex.valueOf((String) model.getValueAt(row, 4));
        PetStatus status = PetStatus.valueOf((String) model.getValueAt(row, 5));
        String description = (String) model.getValueAt(row, 6);

        // Set the pet details in the EditPet form
        editPet.setPetDetails(id, name, age, type, sex, status, description);

        // Display the EditPet form
        editPet.setVisible(true);
        editPet.pack();
        editPet.setLocationRelativeTo(null);
        editPet.setDefaultCloseOperation(javax.swing.JFrame.DISPOSE_ON_CLOSE);

    }

    private void loadPetsToTable() {
        // Clear the table model before loading data
        model.setRowCount(0);

        List<Pet> pets = PetList.getPets();
        for (Pet pet : pets) {
            model.addRow(new Object[]{
                pet.getId().toString(),
                pet.getName(),
                pet.getAge(),
                pet.getType().toString(),
                pet.getSex().toString(),
                pet.getStatus().toString(),
                pet.getDescription()
            });
        }
    }
    
    public static void addRowToTable(Object[] dataRow) {
        
        model.addRow(dataRow);
    }

    public static void updateRowInTable(UUID id, Object[] dataRow) {
        for (int i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 0).equals(id.toString())) {
                for (int j = 0; j < dataRow.length; j++) {
                    model.setValueAt(dataRow[j], i, j);
                }
                break;
            }
        }
    }

    public static void removeRowFromTable(UUID id) {
        for (int i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 0).equals(id.toString())) {
                model.removeRow(i);
                break;
            }
        }
        // Repaint the table to reflect the changes
        petsTable.repaint();
    }
    
